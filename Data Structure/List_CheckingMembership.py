# Checking Membership in a List
# You can use the 'in' keyword to check if an element exists in a list.

# Example list
my_list = ['apple', 'banana', 'cherry', 'strawberry']

# Checking for membership
if 'strawberry' in my_list:
    print('Strawberry is in the list')
else:
    print('Strawberry is not in the list')