# Modifying Dictionary Values
# Dictionaries are mutable, meaning their values can be changed by assigning a new value to a key.

# Example dictionary
my_dict = {'name': 'Musfik', 'age': 25, 'city': 'Shanghai', 'Occupation': 'Data Engineer'}

# Modifying a value
my_dict['age'] = 30
my_dict['Occupation'] = 'Doctor'
# Printing the modified dictionary
print('Modified Dictionary:', my_dict)